package Chapter1;

public class Variables {

    public static void main(String[] args) {
        // Comment
        // Basic addition with int type
        int num1 = 5;
        int num2 = 4;
        System.out.println(num1 + num2);

        // Basic subtraction with double type
        double a = 2.42;
        double b = 1.35;
        System.out.println(a-b);

        // Some age operations
        int onur = 23;
        int adil = 29;
        System.out.println("Onur and Adil age addition is : "  + (onur + adil));
        System.out.println("Onur and Adil age difference is : "  + (adil - onur));


    }
}
