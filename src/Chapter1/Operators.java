package Chapter1;

public class Operators {
    public static void main(String[] args) {

        // Operators -->    + , - , * , / , % = remain
        int a = 5 ;
        int b = 4 ;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b );
        System.out.println(a % b);
        a--;
        b++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(++a);
        System.out.println(--b);

    }
}
